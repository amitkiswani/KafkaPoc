package org.example;



public class ProduceTopic {

    // Topic level
    private String topicName;
    private String keySerializer;
    private String valueSerializer;

    // Cluster level
    private String bootstrapServers;
    private String securityProtocol;
    private String saslMechanism;

    // Cluster + ASL level
    private String username;
    private String password;

    private String maxInFlightRequestPerConnection;
    private String acks;

    public String getTopicName() {
        return topicName;
    }

    public void setTopicName(String topicName) {
        this.topicName = topicName;
    }

    public String getKeySerializer() {
        return keySerializer;
    }

    public void setKeySerializer(String keySerializer) {
        this.keySerializer = keySerializer;
    }

    public String getValueSerializer() {
        return valueSerializer;
    }

    public void setValueSerializer(String valueSerializer) {
        this.valueSerializer = valueSerializer;
    }

    public String getBootstrapServers() {
        return bootstrapServers;
    }

    public void setBootstrapServers(String bootstrapServers) {
        this.bootstrapServers = bootstrapServers;
    }

    public String getSecurityProtocol() {
        return securityProtocol;
    }

    public void setSecurityProtocol(String securityProtocol) {
        this.securityProtocol = securityProtocol;
    }

    public String getSaslMechanism() {
        return saslMechanism;
    }

    public void setSaslMechanism(String saslMechanism) {
        this.saslMechanism = saslMechanism;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getMaxInFlightRequestPerConnection() {
        return maxInFlightRequestPerConnection;
    }

    public void setMaxInFlightRequestPerConnection(String maxInFlightRequestPerConnection) {
        this.maxInFlightRequestPerConnection = maxInFlightRequestPerConnection;
    }

    public String getAcks() {
        return acks;
    }

    public void setAcks(String acks) {
        this.acks = acks;
    }
}
